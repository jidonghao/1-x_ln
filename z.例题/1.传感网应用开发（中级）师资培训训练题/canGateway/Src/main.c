/* USER CODE BEGIN 0 */
void zigbee_receive(uint8_t byte)
{
    uint16_t sensorValue;
    uart4_buf[uart4_rxlen++]=byte;
    if(byte=='\n')
    {
        if(uart4_rxlen==8)
        {
            zigbee_can_data[0]=uart4_buf[0];
            zigbee_can_data[1]=uart4_buf[1];
            zigbee_can_data[2]=uart4_buf[2];
            zigbee_can_data[3]=uart4_buf[3];
            zigbee_can_data[4]=uart4_buf[4];
            zigbee_id = uart4_buf[4];
            zigbee_flag = 1;
            
            sensorValue=uart4_buf[1];
            sensorValue<<=8;
            sensorValue|=uart4_buf[2];
            
            if(sensorValue<200)
                HAL_GPIO_WritePin(ALARM_GPIO_Port,ALARM_Pin,GPIO_PIN_RESET);
            else
                HAL_GPIO_WritePin(ALARM_GPIO_Port,ALARM_Pin,GPIO_PIN_SET);
        }
    }
    if(uart4_rxlen>=8) 
        uart4_rxlen=0;
}
